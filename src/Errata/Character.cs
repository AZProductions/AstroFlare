namespace Errata
{
    /// <summary>
    /// Represents a renderable character.
    /// </summary>
    public enum Character
    {
        /// <summary>
        /// Represents `:`.
        /// </summary>
        Colon,

        /// <summary>
        /// Represents `┌`.
        /// </summary>
        TopLeftCornerHard,

        /// <summary>
        /// Represents `└`.
        /// </summary>
        BottomLeftCornerHard,

        /// <summary>
        /// Represents `├`.
        /// </summary>
        LeftConnector,

        /// <summary>
        /// Represents `─`.
        /// </summary>
        HorizontalLine,

        /// <summary>
        /// Represents `│`.
        /// </summary>
        VerticalLine,

        /// <summary>
        /// Represents `·`.
        /// </summary>
        Dot,

        /// <summary>
        /// Represents `┬`.
        /// </summary>
        Anchor,

        /// <summary>
        /// Represents `─`.
        /// </summary>
        AnchorHorizontalLine,

        /// <summary>
        /// Represents `│`.
        /// </summary>
        AnchorVerticalLine,

        /// <summary>
        /// Represents `╰`.
        /// </summary>
        BottomLeftCornerRound,
    }
}
